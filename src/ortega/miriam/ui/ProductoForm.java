        System.out.println("=>"+this.nombreTxt.getText());
        producto.setNombre(this.nombreTxt.getText());
        producto.setGrupoid((Grupo) grupoCombo.getSelectedItem());
        producto.setPrecioVenta(new BigDecimal(this.venta.getText()));
        producto.setPrecioCompra(new BigDecimal(this.compra.getText()));
        producto.setStock(Integer.valueOf(this.actual.getText()));
        producto.setStockMinimo(Integer.valueOf(this.minimo.getText()));
         
        if (producto.getId() == null) {
            ProductoPanel.crear(producto); 
        } else {
            ProductoPanel.editar(producto); 
        }
        this.dispose();
