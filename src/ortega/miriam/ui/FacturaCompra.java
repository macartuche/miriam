    /**
     *
     * @return
     */
    private static boolean validar() {
        boolean error = false;
        if (proveedor == null) {
            error = true;
            errores.add("- Debe seleccionar un proveedor");
        }
        System.out.println("=>"+lista.size());

        if (lista.isEmpty()) {
            error = true;
            
            errores.add("- Debe seleccionar al menos un producto");
        }

        return error;
    }

   
    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        boolean hayError = validar();
        if (hayError) {
            String errorMsg = "";
            for (String error : errores) {
                errorMsg += error + " \n";
            }
            JOptionPane.showMessageDialog(this,  errorMsg, "Error",JOptionPane.ERROR_MESSAGE);
        }
        
        facturaC.setIdproveedor(proveedor);
        facturaC.setIva(iva);
        facturaC.setSubtotal(subtotal);
        facturaC.setTotal(total);
        facturaC.setNumero(numeroTxt.getText());
        
        if(facturaC.getId() == null){
            controller.create(facturaC);
            //guardar detalle
            for (DetalleTemp lista1 : lista) {
                Detalle detalle = new Detalle();
                detalle.setCantidad(lista1.getCantidad());
                detalle.setIdfactura(facturaC);
                detalle.setIdproducto(lista1.getProducto());
                detalle.setPreciounitario(lista1.getPrecioUnitario());
                detalle.setTotal(lista1.getPrecioTotal());
                controllerDetail.create(detalle);
            }
            JOptionPane.showMessageDialog(this,  "Registro creado con éxito", "Informativo", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }else{
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    public static void recibirDetalle(DetalleTemp detalle) {
        lista.add(detalle);

        //actualizar tabla
        DefaultTableModel model = (DefaultTableModel) detalleTabla.getModel();
        model.addRow(new Object[]{detalle.getCantidad(), detalle.getProducto().getNombre(),
            detalle.getProducto().getPrecioCompra().doubleValue(), detalle.getPrecioTotal().doubleValue()});
        subtotal = subtotal.add(detalle.getPrecioTotal());
        iva = iva.add(detalle.getIvatotal());
        total = subtotal.add(iva);

        //UI labels
        UIactualizar();

    }

    /**
     *
     */
    private static void UIactualizar() {
        subtotalLbl.setText("" + subtotal.setScale(2, RoundingMode.CEILING));
        ivaLbl.setText("" + iva.setScale(2, RoundingMode.CEILING));
        totalLbl.setText("" + total.setScale(2, RoundingMode.CEILING));
    }

