    /**
     *
     * Eliminar el detalle de la factura
     *
     * @param evt
     */
    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DetalleTemp detalle = getIndex();
        lista.remove(detalle);

        //restar la cantidad
        subtotal = subtotal.subtract(detalle.getPrecioTotal());
        iva = iva.subtract(detalle.getIvatotal());
        total = subtotal.add(iva);
        //actualizar
        UIactualizar();

        DefaultTableModel model = (DefaultTableModel) detalleTabla.getModel();
        int index = jTable1.getSelectedRow();
        model.removeRow(index);
    }//GEN-LAST:event_jButton4ActionPerformed

