    private CuentasCxP getIndex() {
        int index = jTable1.getSelectedRow();
        CuentasCxP cta = null;
        if (index != -1) {
            cta = list1.get(index);
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        return cta;
    }

